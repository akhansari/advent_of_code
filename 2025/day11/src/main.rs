use day11::*;

fn main() {
    let input = std::fs::read_to_string("../inputs/day11.txt").expect("Input file not found");
    println!("Part one: {}", part_one(&input));
    let now = std::time::Instant::now();
    println!("Part two: {} in {:.2?}", part_two(&input), now.elapsed());
}

